
function percentage(num, per)
{
  return (num/100)*per;
}

function sum(a, b) {
    return a + b;
}
  
function minus(x, y) {
    return x - y;
}
    
function multiply(x, y) {
    return x * y;
}
 
function divide(x, y) {
    return x / y;
}
  
